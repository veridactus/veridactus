//! # 存储抽象层
//!
//! 提供统一的存储接口，支持多种后端存储系统。
//!
//! ## 架构设计
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────────┐
//! │                    Store Facade                             │
//! │  (提供统一的访问入口，管理多存储后端)                        │
//! └─────────────────────────────────────────────────────────────┘
//!                              │
//!         ┌────────────────────┼────────────────────┐
//!         ▼                    ▼                    ▼
//! ┌───────────────┐    ┌───────────────┐    ┌───────────────┐
//! │ TraceStore   │    │  ConfigStore  │    │  BudgetStore  │
//! │ (追踪存储)   │    │  (配置存储)   │    │  (预算存储)   │
//! └───────────────┘    └───────────────┘    └───────────────┘
//!         │                    │                    │
//!         ▼                    ▼                    ▼
//! ┌─────────────────────────────────────────────────────────────┐
//! │                    Storage Adapters                         │
//! ├───────────────┬───────────────┬───────────────┬───────────┤
//! │ InMemory      │ PostgreSQL    │ Redis         │ S3/MinIO  │
//! │ (开发测试)    │ (主存储)      │ (缓存)        │ (大对象)  │
//! └───────────────┴───────────────┴───────────────┴───────────┘
//! ```
//!
//! ## 核心 Trait
//!
//! - `TraceStore` - Trace 数据的增删改查
//! - `ConfigStore` - 配置数据的存储和同步
//! - `BudgetStore` - 预算状态的实时管理
//! - `AsyncTraceWriter` - 异步写入队列接口

