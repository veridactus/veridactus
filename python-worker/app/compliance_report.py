def compute_traces_merkle_root(traces: List[dict]) -> str:
    """从 Trace 数据计算 Merkle 根"""
    leaves = [
        hashlib.sha256(json.dumps(t, sort_keys=True).encode()).hexdigest()
        for t in traces
    ]
    return compute_merkle_root(leaves)
