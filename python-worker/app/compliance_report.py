    if "--traces" in sys.argv:
        traces_file = sys.argv[sys.argv.index("--traces") + 1]
        with open(traces_file) as f:
            traces = json.load(f)
        leaves = [hashlib.sha256(json.dumps(t, sort_keys=True).encode()).hexdigest() for t in traces]
        computed = compute_merkle_root(leaves)
        merkle_ok = computed == report.get("merkle_root", "")
