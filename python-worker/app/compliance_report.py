"""VERIDACTUS 合规报告 PDF 生成器 (Phase 4)

生成带有 Merkle Root + ZK 证明 + 数字签名的合规报告 PDF，
并提供离线验证 Python 脚本。
"""
import hashlib
import json
import logging
import os
import tempfile
from datetime import datetime
from typing import List, Optional

from fpdf import FPDF

logger = logging.getLogger("veridactus-compliance")


class CompliancePDF(FPDF):
    """VERIDACTUS 合规报告 PDF 生成器"""

    def __init__(self, regulation: str):
        super().__init__(orientation='P', unit='mm', format='A4')
        self.regulation = regulation
        self.set_auto_page_break(auto=True, margin=20)
        # 注册中文字体（如果存在）
        font_paths = [
            '/System/Library/Fonts/STHeiti Light.ttc',  # macOS
            '/usr/share/fonts/truetype/wqy/wqy-zenhei.ttc',  # Linux
            '/usr/share/fonts/opentype/noto/NotoSansCJK-Regular.ttc',  # Linux
        ]
        self.cn_font = None
        for fp in font_paths:
            if os.path.exists(fp):
                self.add_font('CJK', '', fp, uni=True)
                self.cn_font = 'CJK'
                break

    def header(self):
        if self.cn_font:
            self.set_font(self.cn_font, 'B', 10)
        else:
            self.set_font('Helvetica', 'B', 10)
        self.set_text_color(108, 92, 231)
        self.cell(0, 8, 'VERIDACTUS Compliance Report', align='C', new_x="LMARGIN", new_y="NEXT")
        self.set_draw_color(108, 92, 231)
        self.line(self.l_margin, self.get_y(), self.w - self.r_margin, self.get_y())
        self.ln(4)

    def footer(self):
        self.set_y(-15)
        if self.cn_font:
            self.set_font(self.cn_font, '', 8)
        else:
            self.set_font('Helvetica', '', 8)
        self.set_text_color(128, 128, 128)
        self.cell(0, 10, f'Page {self.page_no()}/{{nb}} | Generated by VERIDACTUS', align='C')

    def section_title(self, title: str):
        if self.cn_font:
            self.set_font(self.cn_font, 'B', 13)
        else:
            self.set_font('Helvetica', 'B', 13)
        self.set_text_color(108, 92, 231)
        self.cell(0, 10, title, new_x="LMARGIN", new_y="NEXT")
        self.set_draw_color(108, 92, 231, 50)
        self.line(self.l_margin, self.get_y(), self.w - self.r_margin, self.get_y())
        self.ln(4)

    def body_text(self, text: str):
        if self.cn_font:
            self.set_font(self.cn_font, '', 9)
        else:
            self.set_font('Helvetica', '', 9)
        self.set_text_color(60, 60, 60)
        self.multi_cell(0, 5, text)
        self.ln(2)

    def key_value(self, key: str, value: str):
        if self.cn_font:
            self.set_font(self.cn_font, 'B', 9)
        else:
            self.set_font('Helvetica', 'B', 9)
        self.set_text_color(80, 80, 80)
        self.cell(50, 6, key + ':')
        if self.cn_font:
            self.set_font(self.cn_font, '', 9)
        else:
            self.set_font('Helvetica', '', 9)
        self.set_text_color(40, 40, 40)
        self.cell(0, 6, str(value), new_x="LMARGIN", new_y="NEXT")

    def status_badge(self, text: str, ok: bool):
        color = (0, 212, 170) if ok else (255, 107, 107)
        self.set_fill_color(*color)
        self.set_text_color(255, 255, 255)
        if self.cn_font:
            self.set_font(self.cn_font, 'B', 8)
        else:
            self.set_font('Helvetica', 'B', 8)
        self.cell(24, 5, 'PASS' if ok else 'FAIL', fill=True, align='C')
        self.set_text_color(60, 60, 60)
        if self.cn_font:
            self.set_font(self.cn_font, '', 9)
        else:
            self.set_font('Helvetica', '', 9)
        self.cell(0, 5, '  ' + text, new_x="LMARGIN", new_y="NEXT")
        self.ln(1)


def generate_compliance_report(
    trace_ids: List[str],
    regulation: str,
    traces_data: List[dict],
    output_dir: str = "",
) -> dict:
    """
    生成合规报告 PDF。

    参数:
        trace_ids: Trace ID 列表
        regulation: 法规名称 (EU_AI_ACT | GDPR | NIST_AI_600)
        traces_data: Trace 数据列表
        output_dir: PDF 输出目录（默认临时目录）

    返回:
        report_metadata: 报告元数据（含 pdf_path 和 offline_script_path）
    """
    if not traces_data:
        logger.warning("No trace data for compliance report")
        return {"status": "empty", "trace_count": 0}

    if not output_dir:
        output_dir = tempfile.gettempdir()

    report_id = f"compliance-{datetime.utcnow().strftime('%Y%m%d-%H%M%S')}"

    # 1. 聚合 Merkle Root (L2A)
    merkle_leaves = []
    for trace in traces_data:
        trace_json = json.dumps(trace, sort_keys=True)
        leaf_hash = hashlib.sha256(trace_json.encode()).hexdigest()
        merkle_leaves.append(leaf_hash)
    aggregate_merkle_root = compute_merkle_root(merkle_leaves)

    # 2. 收集 ZK 证明 (L2B)
    zk_proofs = []
    for trace in traces_data:
        proofs = trace.get("proofs", {})
        proof_chain = proofs.get("proof_chain", [])
        for entry in proof_chain:
            if entry.get("level") == "L2B" and entry.get("zk_proof"):
                zk_proofs.append({
                    "trace_id": trace.get("trace_id"),
                    "zk_proof_hash": hashlib.sha256(entry["zk_proof"].encode()).hexdigest()[:16],
                })

    # 3. 数字签名
    report_data_str = json.dumps({
        "merkle_root": aggregate_merkle_root,
        "trace_count": len(traces_data),
        "zk_proof_count": len(zk_proofs),
        "regulation": regulation,
        "generated_at": datetime.utcnow().isoformat(),
    }, sort_keys=True)
    signature = hashlib.sha256(report_data_str.encode()).hexdigest()

    # 4. 合规映射
    compliance_mapping = get_compliance_mapping(regulation)

    # 5. 离线验证脚本
    verify_script = generate_verify_script(aggregate_merkle_root, signature)
    verify_script_path = os.path.join(output_dir, f"{report_id}_verify.py")
    with open(verify_script_path, 'w') as f:
        f.write(verify_script)
    logger.info(f"Offline verification script: {verify_script_path}")

    # 6. 生成真实 PDF 报告
    pdf = CompliancePDF(regulation)
    pdf.alias_nb_pages()
    pdf.add_page()

    # 报告标题
    pdf.section_title(f'Compliance Report')
    pdf.body_text(f'Regulation: {regulation}')
    pdf.body_text(f'Generated: {datetime.utcnow().strftime("%Y-%m-%d %H:%M:%S UTC")}')
    pdf.body_text(f'Report ID: {report_id}')
    pdf.ln(4)

    # 摘要
    pdf.section_title('Summary')
    pdf.key_value('Total Traces', str(len(traces_data)))
    pdf.key_value('ZK Proofs', str(len(zk_proofs)))
    pdf.key_value('Merkle Root', aggregate_merkle_root[:32] + '...')
    pdf.key_value('Signature', signature[:32] + '...')
    pdf.ln(4)

    # 合规条款
    pdf.section_title(f'Compliance Mapping ({regulation})')
    for item in compliance_mapping:
        pdf.status_badge(
            f'Art.{item["article"]}: {item["requirement"]}',
            item["satisfied"]
        )
    pdf.ln(4)

    # Trace 列表
    pdf.section_title('Trace Records')
    if pdf.cn_font:
        pdf.set_font(pdf.cn_font, '', 8)
    else:
        pdf.set_font('Helvetica', '', 8)
    for i, trace in enumerate(traces_data[:50]):
        tid = trace.get('trace_id', str(i))[:24]
        model = trace.get('model', '?')
        state = trace.get('execution_state', '?')
        pdf.set_text_color(100, 100, 100)
        pdf.cell(0, 4, f'  {tid}  |  {model}  |  {state}', new_x="LMARGIN", new_y="NEXT")
    if len(traces_data) > 50:
        pdf.set_text_color(108, 92, 231)
        pdf.cell(0, 5, f'  ... and {len(traces_data) - 50} more traces', new_x="LMARGIN", new_y="NEXT")
    pdf.ln(4)

    # ZK 证明
    if zk_proofs:
        pdf.section_title('ZK Proofs (L2B)')
        for p in zk_proofs[:20]:
            pdf.key_value(p.get('trace_id', '?')[:24], p.get('zk_proof_hash', '?'))
        pdf.ln(4)

    # 密码学保证
    pdf.section_title('Cryptographic Assurance')
    pdf.body_text('This report is cryptographically signed and verifiable offline.')
    pdf.body_text(f'L2A Merkle Root: {aggregate_merkle_root}')
    pdf.body_text(f'Digital Signature (SHA-256): {signature}')
    pdf.ln(2)
    pdf.body_text('To verify this report:')
    pdf.body_text(f'  python3 {report_id}_verify.py')
    pdf.body_text('Verification is performed entirely offline — no trust in VERIDACTUS servers required.')

    # 保存 PDF
    pdf_path = os.path.join(output_dir, f"{report_id}.pdf")
    pdf.output(pdf_path)
    logger.info(f"Compliance PDF report: {pdf_path}")

    # 同时保存 JSON 元数据
    report_metadata = {
        "report_id": report_id,
        "regulation": regulation,
        "trace_count": len(traces_data),
        "merkle_root": aggregate_merkle_root,
        "zk_proof_count": len(zk_proofs),
        "compliance_articles": len(compliance_mapping),
        "all_satisfied": all(c["satisfied"] for c in compliance_mapping),
        "signature": signature,
        "generated_at": datetime.utcnow().isoformat(),
        "pdf_path": pdf_path,
        "offline_verify_script": verify_script_path,
        "trace_ids": trace_ids[:100],
    }

    json_path = os.path.join(output_dir, f"{report_id}.json")
    with open(json_path, 'w') as f:
        json.dump(report_metadata, f, indent=2, ensure_ascii=False)

    return report_metadata


def compute_merkle_root(leaves: List[str]) -> str:
    """计算 Merkle 根哈希"""
    if not leaves:
        return hashlib.sha256(b"").hexdigest()
    if len(leaves) == 1:
        return leaves[0]
    current = leaves[:]
    while len(current) > 1:
        next_level = []
        for i in range(0, len(current), 2):
            left = current[i]
            right = current[i + 1] if i + 1 < len(current) else left
            next_level.append(hashlib.sha256((left + right).encode()).hexdigest())
        current = next_level
    return current[0]


